// data_types.js

// Number
let age = 30;
console.log('Age:', age);

// String
let name = 'John';
console.log('Name:', name);

// Boolean
let isStudent = true;
console.log('Is Student:', isStudent);

// Array
let numbers = [1, 2, 3, 4, 5];
console.log('Numbers Array:', numbers);

// Object
let person = {
    name: 'John',
    age: 30
};
console.log('Person Object:', person);

// Null and Undefined
let nothing = null;
let notDefined;
console.log('Null value:', nothing);
console.log('Undefined value:', notDefined);
